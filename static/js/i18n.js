let currentLang = localStorage.getItem("crm_lang") || "zh";

function t(key) {
  return (TRANSLATIONS[currentLang] || TRANSLATIONS.zh)[key] || TRANSLATIONS.zh[key] || key;
}

function setLang(lang) {
  if (!["zh", "en", "ja"].includes(lang)) return;
  currentLang = lang;
  localStorage.setItem("crm_lang", lang);
  applyTranslations();
  updateLangButtons();
  if (window._currentPage) renderPage(window._currentPage);
}

function applyTranslations() {
  document.querySelectorAll("[data-i18n]").forEach(el => {
    el.textContent = t(el.dataset.i18n);
  });
  document.querySelectorAll("[data-i18n-ph]").forEach(el => {
    el.placeholder = t(el.dataset.i18nPh);
  });
  document.documentElement.lang = currentLang === "ja" ? "ja" : currentLang === "en" ? "en" : "zh-CN";
}

function updateLangButtons() {
  document.querySelectorAll("[data-lang]").forEach(btn => {
    btn.classList.toggle("active", btn.dataset.lang === currentLang);
  });
}
